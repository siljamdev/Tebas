	public static bool askDeletionConfirmation(){
		initializeConfig();
		if(config.CanGetCampAsBool("deletionConfirmationNeeded", out bool b) && !b){
			return true;
		}
		Console.WriteLine("Are you sure you want to delete it? (Y/N)");
		string ans = Console.ReadLine();
		
		if(ans.ToLower() == "y"){
			return true;
		}
		return false;
	}
	
